function populateStationSelect(select, includeOther) {
    const majorGroup = document.createElement('optgroup');
    majorGroup.label = '主要大站';
    const otherGroup = document.createElement('optgroup');
    otherGroup.label = '其他車站';

    stationsDB.forEach(st => {
        const opt = document.createElement('option');
        opt.value = st.name;
        opt.textContent = st.name;
        if (majorStations.includes(st.name)) {
            majorGroup.appendChild(opt);
        } else {
            otherGroup.appendChild(opt);
        }
    });

    select.appendChild(majorGroup);
    select.appendChild(otherGroup);

    if (includeOther) {
        const otherOpt = document.createElement('option');
        otherOpt.value = '其他';
        otherOpt.textContent = '其他...';
        select.appendChild(otherOpt);
    }
}

